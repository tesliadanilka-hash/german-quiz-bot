def load_words():
    global WORDS, TOPICS
    with open(WORDS_FILE, "r", encoding="utf-8") as f:
        WORDS = json.load(f)

    topics_words: Dict[str, List[int]] = defaultdict(list)

    for idx, w in enumerate(WORDS):
        text = (w.get("de", "") + " " + w.get("ru", "")).lower()
        assigned = False
        for rule in TOPIC_RULES:
            if any(kw in text for kw in rule["keywords"]):
                topics_words[rule["id"]].append(idx)
                assigned = True
                break
        if not assigned:
            topics_words[THEME_OTHER].append(idx)

    TOPICS = {}
    for rule in TOPIC_RULES:
        ids = topics_words.get(rule["id"], [])
        if ids:
            TOPICS[rule["id"]] = {
                "id": rule["id"],
                "name": rule["name"],
                "words": ids,
                "count": len(ids),
            }

    # –¢–µ–º–∞ "–†–∞–∑–Ω–æ–µ"
    other_ids = topics_words.get(THEME_OTHER, [])
    if other_ids:
        TOPICS[THEME_OTHER] = {
            "id": THEME_OTHER,
            "name": "–†–∞–∑–Ω–æ–µ",
            "words": other_ids,
            "count": len(other_ids),
        }


